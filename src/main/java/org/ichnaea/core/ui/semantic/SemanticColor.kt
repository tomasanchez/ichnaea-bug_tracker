    companion object {
        val PRIMARY: Color = Color.decode("#0d6efd")
        val SECONDARY: Color = Color.decode("#6c757d")
        val SUCCESS: Color = Color.decode("#198754")
        val DANGER: Color = Color.decode("#dc3545")
        val WARNING: Color = Color.decode("#ffc107")
        val INFO: Color = Color.decode("#0dcaf0")
        val LIGHT: Color = Color.decode("#f8f9fa")
        val DARK: Color = Color.decode("#212529")
    }

}